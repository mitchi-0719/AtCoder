N = I()
h = LI()


def f(l, r, b):
    if r < l:
        return 0

    if r == l:
        return h[l] - b

    mn = min(h[l:r])
    idx = [i for i in range(N) if h[i] == mn]
    ans = mn - b
    for i in range(len(idx) - 1):
        l2, r2 = idx[i], idx[i + 1]
        ans += f(l2 + 1, r2 - 1, mn - b)

    return ans


print(f(0, N, 0))
